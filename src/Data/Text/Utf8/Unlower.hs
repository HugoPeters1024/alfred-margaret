-- | Inverse of Char.toLower/Utf8.lowerCodePoint
--
-- Returns all the characters that have the given character as their lower case, for example:
--
--    unlowerCodePoint 'a' == "aA"
--    unlowerCodePoint 'A' == ""
--    unlowerCodePoint '1' == "1"
--    unlowerCodePoint 'i' == "İiI"
--    unlowerCodePoint 'ß' == "ẞß"
--
unlowerCodePoint :: Char -> [Char]
unlowerCodePoint c =
  maybe [c] id $ HashMap.lookup c unlowerings


-- | Reference implementation of unlowerCodePoint, simple but slow.
-- This is useful fo
refUnlowerCodePoint :: Char -> [Char]
refUnlowerCodePoint =
  \c -> maybe [] id $ HashMap.lookup c completeMapping
  where
    addUnlowering c hm =
      HashMap.insertWith (++) (Char.toLower c) [c] hm
    completeMapping =
      List.foldl' (flip addUnlowering) HashMap.empty [minBound..maxBound]


-- | This function generates the definition of 'unlowerings', so that you can paste it below.
printUnlowerings :: IO ()
printUnlowerings = do

  let
    showCP :: Char -> String
    showCP c = case Char.ord c of
      co | co > 68000 -> show co -- Some RTL languages above these code points are annoying to print
      co -> c : " (" <> show co <> ")"

    showCPs :: [Char] -> String
    showCPs cs = List.intercalate " " (map showCP cs)

    charTuple lc = (lc, refUnlowerCodePoint lc)
    includeInHashMap (lc, ucs) = ucs /= [lc]   -- Include in hashmap if it's not just id

    category (lc, ucs)
      | ucs == [lc, Char.toUpper lc] = Simple
      | ucs == [Char.toUpper lc, lc] = Simple
      | ucs == [] = AlreadyUppercase
      | otherwise = Special

    lst :: [(Char, [Char])]
    lst = List.sortOn ((,) <$> category <*> fst) $ filter includeInHashMap $ map charTuple $ [minBound..maxBound]

  putStrLn "  HashMap.fromList"
  forM_ (zip ('[':repeat ',') lst) $ \(prefixChar, (lc, ucs)) -> do
    let
      comment = case category (lc, ucs) of
        Special -> "SPECIAL: " <> showCP lc <> " -> " <> showCPs ucs
        AlreadyUppercase -> "ALREADY UPPERCASE: " <> showCP lc
        Simple -> showCP lc <> " -> " <> List.intercalate " " (map showCP ucs)
    -- putStrLn $ "    " <> [prefixChar] <> " (" <> show lc <> ", " <> show ucs <> "\")  -- " <> comment
    putStrLn $ "    " <> [prefixChar] <> " " <> show (lc, ucs) <> "  -- " <> comment
  putStrLn "    ]"

data UnloweringCategory
  = Special
  | AlreadyUppercase
  | Simple
  deriving (Show, Ord, Eq)



